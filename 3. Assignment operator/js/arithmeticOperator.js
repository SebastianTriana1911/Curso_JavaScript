// ARITHMETIC OPERATOR

let value1 = 10;
let value2 = 5;

// addition "Sum"
console.log(value1 + value2);

// subtraction "rest"
console.log(value1 - value2);

// Multiplication
console.log(value1 * value2);

// Division
console.log(value1 / value2);

// Module
console.log(value1 % value2);

// Exponentiation
console.log(value1 ** value2);

// Increment operator
let a = 2;
a++;
console.log(a);

// Decrement operator
let b = 10;
b--;
console.log(b);

