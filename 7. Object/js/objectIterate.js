// ITERATE OBJECT

const tablet = {
    reference: 12345,
    dimensions: "10.2 inches",
    memory: "64GB",
    price: 300, 
};

// Object.keys(): Return a array with the names od the properties from object.
console.log(Object.keys(tablet));

// Object.values(): Return a array with the values corresponding to the properties from object.
console.log(Object.values(tablet));

// Object.entries(): Return a array of the keys and value of the given object
console.log(Object.entries(tablet));

