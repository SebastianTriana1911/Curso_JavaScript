// Variables in JavaScript

// Basic example of variables
a = 5;
b = 15;
resultado = a + b;
console.log(resultado);

// Variables with the let reserved word
let sumando1 = 150;
let sumando2 = 350;
console.log(sumando1+sumando2);

// With the reserved word let we cannot declare the same variable several times.
let descuento = 120;
descuento = 100;
console.log(descuento);

// It is not mandatory to initialize the variables, we can declare them and assign them a value later.
let nombre = "Sebastian";
let edad; // Undifined
console.log(nombre);
console.log(edad);

